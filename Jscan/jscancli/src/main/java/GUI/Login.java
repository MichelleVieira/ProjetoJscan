    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            UserDAO user = new UserDAO();

            Funcionario.setNmEmail(jTextField1.getText());
            Funcionario.setNmSenha(jTextField2.getText());

            if (user.Get()) {
                if(Funcionario.getIdTipo() == 1){
                    jLabel3.setText("NÃO DISPONÍVEL PARA GESTORES");
                } else {
                    dispose();
                    TelaOshi oshi = new TelaOshi();
                    oshi.setVisible(true);
                }
            } else {
                jLabel3.setText("login e/ou senha inválidos");
            }
        } catch (SQLException ex) {
            Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
            //implementar log
        }
    }//GEN-LAST:event_jButton1ActionPerformed

