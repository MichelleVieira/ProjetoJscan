    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        Conexao conect = new Conexao();
        login = jTextField1.getText();
        senha = jTextField2.getText();
        List<String> usuario = conect.conectaai("SELECT nmEmail, nmSenha FROM TB_FUNCIONARIO WHERE NMCARGO = 'dev' AND nmEmail = '" + login + "'");

        if (login == null || login.isEmpty() && senha == null || senha.isEmpty()) {
            jLabel3.setText("por favor, informe o negocio");
        } else if (!usuario.isEmpty()) {

            if (usuario.get(0).equals(login)) {

                if (usuario.get(1).equals(senha)) {
                    dispose();
                    Api teste = new Api();
                    teste.setVisible(true);
                } else {
                    jLabel3.setText("login ou senha inválido");
                }
            } else {
                jLabel3.setText("login ou senha inválido");
            }
        } else {
            jLabel3.setText("login ou senha inválido");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

